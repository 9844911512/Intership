def Pairs(ls, p): 
    res = []
    while ls:
        num = ls.pop()
        diff = p - num
        if diff in ls:
            res.append((diff, num))
         
    res.reverse()
    return res
     
ls = [1, 5, 3, 7, 9]
p= 12
print(Pairs(ls, p))


